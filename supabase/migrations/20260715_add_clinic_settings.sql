create table if not exists public.clinic_settings (
  id text primary key default 'as-estetica',
  slug text not null default 'clinica-bela',
  name text not null default 'AS Estética',
  description text,
  address text,
  phone text,
  email text,
  website text,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now()
);

create table if not exists public.clinic_working_hours (
  id bigint generated by default as identity primary key,
  clinic_id text not null default 'as-estetica' references public.clinic_settings(id) on delete cascade,
  weekday integer not null check (weekday between 0 and 6),
  is_open boolean not null default false,
  opens_at text,
  closes_at text,
  pause_starts_at text,
  pause_ends_at text,
  created_at timestamp with time zone not null default now(),
  updated_at timestamp with time zone not null default now(),
  unique (clinic_id, weekday)
);

