do $$
begin
  if not exists (
    select 1 from pg_policies
    where schemaname = 'public'
      and tablename = 'clinic_settings'
      and policyname = 'Anyone can read clinic settings'
  ) then
    create policy "Anyone can read clinic settings"
      on public.clinic_settings
      for select
      to anon, authenticated
      using (true);
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public'
      and tablename = 'clinic_settings'
      and policyname = 'Authenticated can manage clinic settings'
  ) then
    create policy "Authenticated can manage clinic settings"
      on public.clinic_settings
      for all
      to authenticated
      using (true)
      with check (true);
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public'
      and tablename = 'clinic_working_hours'
      and policyname = 'Anyone can read clinic working hours'
  ) then
    create policy "Anyone can read clinic working hours"
      on public.clinic_working_hours
      for select
      to anon, authenticated
      using (true);
  end if;

  if not exists (
    select 1 from pg_policies
    where schemaname = 'public'
      and tablename = 'clinic_working_hours'
      and policyname = 'Authenticated can manage clinic working hours'
  ) then
    create policy "Authenticated can manage clinic working hours"
      on public.clinic_working_hours
      for all
      to authenticated
      using (true)
      with check (true);
  end if;
end $$;
